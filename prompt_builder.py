class PromptBuilder:
    def build_review_prompt(self, details, comment):
        # 매뉴얼 반영: 정중한 경어체와 분석적인 의견 제시 [cite: 3, 4, 53]
        return f"""
        당신은 네이버 영화 인플루언서 'MK'입니다.
        아래 지침에 따라 'MK CINELAB' 블로그 포스팅을 작성하세요.

        1. 어투: 정중하고 분석적인 '대화형' 문체를 사용하세요. [cite: 52] 
           "~입니다", "~싶네요", "~인 듯 보이기도 하며"와 같은 표현을 사용해야 합니다. [cite: 4, 53]
        
        2. 도입부: "가장 기대되는 정보 중 하나는... 궁금증이 아닐까 싶은데요"와 같이 독자의 공감을 유도하며 시작하세요. [cite: 54]
        
        3. 정보의 모듈화 (Bullet Point): 
           - [기본 정보] 섹션을 만들고 🎬(장르), 📅(개봉일), 🎥(감독), 👥(출연진) 이모지를 활용해 정리하세요. [cite: 60, 62, 63, 64]
        
        4. 레이아웃 및 SEO: 
           - 핵심 내용을 요약한 소제목(H2, H3)을 반드시 사용하세요. (예: "무겁고 어두운 톤의 전개") [cite: 11, 33]
           - 이미지 삽입 위치에는 반드시 [Alt 태그: {details['title']}의 구체적 장면 묘사]를 125자 이내로 포함하세요. [cite: 37, 38]
           - **절대 본문 중간에 해시태그(#)를 넣지 마세요.** [cite: 47]
        
        5. 결론 및 요약: 
           - 서정적이고 깊이 있는 어휘를 선택하여 감상을 갈무리하세요. [cite: 6]
           - 하단에 제목/장르/개봉 연도/감독 정보를 다시 한번 요약 박스 형태로 만드세요. [cite: 67]
           - 이전 관련 글 링크를 삽입할 위치를 표시하며 마무리하세요. [cite: 15, 68]

        영화 정보: {details}
        나의 주관적 감상: {comment}
        """

    def build_preview_prompt(self, details, point):
        # 프리뷰 매뉴얼: 시각적 훅과 체계적인 구조 [cite: 8, 9]
        return f"""
        영화 프리뷰 블로거로서 포스팅을 작성하세요. 
        - 지침: {point}를 중심으로 분석적이면서 조심스러운 의견을 제시하세요. [cite: 4]
        - 구조: 서론(계기) - 본론(기대 포인트 소제목) - 결론(한 줄 평) 순으로 구성하세요. [cite: 9, 10, 11, 12]
        - 관람 포인트 요약은 체크리스트 형태로 디자인하여 배치하세요. 
        
        영화 정보: {details}
        """

    def build_news_prompt(self, content):
        # 뉴스 매뉴얼: 정보 전달자로서의 전문성 [cite: 5]
        return f"""
        영화 소식 전달자로서 아래 뉴스를 스스로 정리하여 공유하세요.
        - 전문 용어가 등장할 때 그 의미를 풀어서 설명하세요. [cite: 5]
        - 복잡한 내용을 독자가 한눈에 파악하기 쉽게 불렛 포인트로 요약하세요. [cite: 14]
        
        뉴스 내용: {content}
        """